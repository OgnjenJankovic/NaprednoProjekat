    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {                                         
        try {
            if (txtIme.getText().isEmpty() || txtPrezime.getText().isEmpty()
                    || txtEmail.getText().isEmpty()
                    || txtTelefon.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
                return;
            }

            String ime = txtIme.getText();
            String prezime = txtPrezime.getText();
            String email = txtEmail.getText();
            String telefon = txtTelefon.getText();
            Tip tip = (Tip) cmbTip.getSelectedItem();

            Korisnik k = new Korisnik(null, ime, prezime, email, telefon,tip);

            ClientController.getInstance().addKorisnik(k);
            MainForm mf = (MainForm) getParent();
            mf.popuniKorisnike();
            JOptionPane.showMessageDialog(this, "Uspesno dodat korisnik.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }                                        

