    private void popuniKombo() {
        try {
            ArrayList<Tip> tipovi = ClientController.getInstance().getAllTip();

            cmbTip.removeAllItems();

            for (Tip tip : tipovi) {
                cmbTip.addItem(tip);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
